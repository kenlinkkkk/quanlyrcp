    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        try {
            BanveCtr bc = new BanveCtr();
            veFrm vf = new veFrm();
            ArrayList<choNgoiClass> listcn = vf.listCngoi;
            for (int i = 0; i < listcn.size(); i++) {
                veClass ve1 = new veClass();
                if (listcn.get(i).getLoai().equals("Thuong")) {
                    ve1.setGia(45000);
                    
                    choNgoiClass cn = new choNgoiClass();
                    cn.setMaCho(listcn.get(i).getMaCho());
                    ve1.setCngoi(cn);
                    
                    lichChieuClass lc = new lichChieuClass();
                    lc.setMalich(sff.malich);
                    ve1.setLchieu(lc);
                    
//                    hoaDonClass hd = new hoaDonClass();
                    hdon.setMaHD(bc.getLastIDHD());
                    ve1.setHdon(hdon);
                    System.out.println(ve1.getGia() + "-" + (int)ve1.cngoi.getMaCho() + "-" + (int)ve1.lchieu.getMalich() + "--" + (int)ve1.hdon.getMaHD());
                    bc.addVe(ve1);
//                    System.out.println(ve1.toString());
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(hoadonFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_btnConfirmActionPerformed

