    public void loadtblHoadon() throws SQLException{
        veFrm vf = new veFrm();
        ArrayList<choNgoiClass> listcn = vf.listCngoi;
        for (int i = 0; i < listcn.size(); i++) {
            if (listcn.get(i).getLoai().equals("Thuong")) {
                tb2.insertRow(i, new Object[]{i+1,sff.tenphim,sff.thoigian.toString(),sff.tenphong,45000,"Ghế: " + listcn.get(i).getHang() + listcn.get(i).getCot()});
                tong += 45000;
            }else{
                tb2.insertRow(i, new Object[]{i+1,sff.tenphim,sff.thoigian.toString(),sff.tenphong,55000,"Ghế: " + listcn.get(i).getHang() + listcn.get(i).getCot()});
                tong += 55000;
            }
        }
        tongtien.setText("Tổng tiền: " + tong);
    }
    
    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if (!txtKey.getText().equals("")) {
            String key = txtKey.getText();
            ClientCtr clientCtr = new ClientCtr();
            try {
                list = clientCtr.SearchClient(key);
                for(int i = 0;i < list.size();i++){
                    tableModel.insertRow(i, new Object[]{list.get(i).getMaKH(), list.get(i).getTenKH(), list.get(i).getSdt(), list.get(i).getEmail(), list.get(i).getDiachi()});
                }
            } catch (SQLException ex) {
                Logger.getLogger(hoadonFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

